#!/usr/bin/env node

/**
 * Test Fly.io Deployment
 * This script tests the Fly.io deployment functionality
 */

const testCode = `
import React from 'react';

function TestApp() {
  return (
    <div className="min-h-screen bg-gradient-to-br from-blue-500 to-purple-600 flex items-center justify-center">
      <div className="bg-white p-8 rounded-lg shadow-xl max-w-md mx-auto">
        <h1 className="text-3xl font-bold text-gray-800 mb-4">
          🚀 VULK + Fly.io Test
        </h1>
        <p className="text-gray-600 mb-6">
          This app was generated by VULK and deployed to Fly.io!
        </p>
        <div className="space-y-2">
          <div className="flex items-center gap-2 text-green-600">
            <span>✅</span>
            <span>Generated by VULK</span>
          </div>
          <div className="flex items-center gap-2 text-blue-600">
            <span>🌍</span>
            <span>Deployed to Fly.io</span>
          </div>
          <div className="flex items-center gap-2 text-purple-600">
            <span>⚡</span>
            <span>Global CDN</span>
          </div>
        </div>
      </div>
    </div>
  );
}

export default TestApp;
`;

async function testFlyioDeployment() {
  console.log('🧪 Testing Fly.io deployment...\n');

  try {
    const response = await fetch('http://localhost:3000/api/flyio/deploy', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        code: testCode,
        uiId: 'test-' + Date.now(),
        userId: 'test-user',
        sessionId: 'test-session',
        projectName: 'VULK Fly.io Test'
      }),
    });

    const result = await response.json();

    if (result.success) {
      console.log('✅ Deployment successful!');
      console.log(`🌐 Live URL: ${result.projectUrl}`);
      console.log(`📱 App Name: ${result.appName}`);
      console.log(`🆔 Deployment ID: ${result.deploymentId}`);
      
      if (result.logs && result.logs.length > 0) {
        console.log('\n📋 Deployment Logs:');
        result.logs.forEach(log => console.log(`   ${log}`));
      }
    } else {
      console.log('❌ Deployment failed!');
      console.log(`Error: ${result.error}`);
      console.log(`Message: ${result.message}`);
    }
  } catch (error) {
    console.error('❌ Test failed:', error.message);
    console.log('\n💡 Make sure your VULK server is running:');
    console.log('   npm run dev');
  }
}

// Run the test
testFlyioDeployment();
